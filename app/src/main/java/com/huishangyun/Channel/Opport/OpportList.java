package com.huishangyun.Channel.Opport;

public class OpportList {

	private Integer ID;
	private String Title;
	private Integer Status;
	private String Member_Name;
	private double ForecastMoney;
	private Integer SalesStage;
	private Integer Clue_ID;
	private String ForecastDate;
	
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public Integer getStatus() {
		return Status;
	}
	public void setStatus(Integer status) {
		Status = status;
	}
	public String getMember_Name() {
		return Member_Name;
	}
	public void setMember_Name(String member_Name) {
		Member_Name = member_Name;
	}
	public double getForecastMoney() {
		return ForecastMoney;
	}
	public void setForecastMoney(double forecastMoney) {
		ForecastMoney = forecastMoney;
	}
	public Integer getSalesStage() {
		return SalesStage;
	}
	public void setSalesStage(Integer salesStage) {
		SalesStage = salesStage;
	}
	public Integer getClue_ID() {
		return Clue_ID;
	}
	public void setClue_ID(Integer clue_ID) {
		Clue_ID = clue_ID;
	}
	public String getForecastDate() {
		return ForecastDate;
	}
	public void setForecastDate(String forecastDate) {
		ForecastDate = forecastDate;
	}
	
	
}
