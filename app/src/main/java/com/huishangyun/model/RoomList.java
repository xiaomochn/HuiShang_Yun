package com.huishangyun.model;

public class RoomList {
	private Integer ID;
	private String NaturalName;
	private String ServiceName;
	private String OfServiceName;
	private String OfName;
	
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getNaturalName() {
		return NaturalName;
	}
	public void setNaturalName(String naturalName) {
		NaturalName = naturalName;
	}
	public String getServiceName() {
		return ServiceName;
	}
	public void setServiceName(String serviceName) {
		ServiceName = serviceName;
	}
	public String getOfServiceName() {
		return OfServiceName;
	}
	public void setOfServiceName(String ofServiceName) {
		OfServiceName = ofServiceName;
	}
	public String getOfName() {
		return OfName;
	}
	public void setOfName(String ofName) {
		OfName = ofName;
	}
	
	
}
